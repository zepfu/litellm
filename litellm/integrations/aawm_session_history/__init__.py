"""AAWM session_history package.

Owns durable persistence concerns as disjoint modules:

- `runtime.py` — process-local queue/pool/spool/retry state + host bridge
- `writer.py` — bounded queue, daemon worker, batch flush, asyncpg pool lifecycle
- `spool.py` — filesystem degraded-mode spool + drainer
- `retry.py` — flush retry/backoff/exhaustion policy
- `record.py` — record builders, shared field derivation, persist entrypoints
- `sql.py` — DDL/DML constants
- `identity_selection.py` — ordered first-match selection for scripts

Underscore APIs are intentionally re-exported from this package root via an
explicit ``__all__`` (not bare ``import *``), so private helper names remain
importable as ``litellm.integrations.aawm_session_history._enqueue_...`` and
SQL constants as ``..._AAWM_SESSION_HISTORY_INSERT_SQL``.

Agent identity extraction primitives remain in
`litellm.integrations.aawm_agent_identity`, which also re-exports package
surfaces for historical script/test imports.
"""

